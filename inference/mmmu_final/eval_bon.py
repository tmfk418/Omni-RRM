#!/usr/bin/env python3
# -*- coding: utf-8 -*-
"""
eval_accuracy.py
───────────────────────────────────────────────
Batch evaluation of results generated by pairwise_best_from_qwen_runs.
Computes the match rate between selected_best and gt_answer.
"""

import jsonlines
import argparse
import logging
import json

logging.basicConfig(level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(message)s", datefmt="%H:%M:%S")

def eval_accuracy(jsonl_file: str):
    total = 0
    valid = 0
    matched = 0
    error_count = 0

    with jsonlines.open(jsonl_file, "r") as reader:
        for record in reader:
            total += 1
            gt = record.get("gt_answer")
            pred = record.get("selected_best")

            if not gt or gt == "error":
                continue
            if not pred or pred == "error":
                error_count += 1
                continue

            valid += 1
            if pred == gt:
                matched += 1

    accuracy = matched / valid if valid > 0 else 0.0
    logging.info(f"Total samples: {total}")
    logging.info(f"Valid samples: {valid}")
    logging.info(f"Matched: {matched}")
    logging.info(f"Errors (pred=error): {error_count}")
    logging.info(f"Accuracy: {accuracy:.4f}")
    return {
        "total": total,
        "valid": valid,
        "matched": matched,
        "error_count": error_count,
        "accuracy": accuracy
    }

if __name__ == "__main__":
    parser = argparse.ArgumentParser(description="Evaluate accuracy of selected_best vs gt_answer")
    parser.add_argument(
        "--input", 
        default="/path/to/best_from_runs.jsonl", 
        help="Path to model output JSONL file"
    )
    args = parser.parse_args()

    results = eval_accuracy(args.input)
    print(json.dumps(results, indent=2))
